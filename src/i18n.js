export function detectLocale() {
  const raw = Intl?.DateTimeFormat?.().resolvedOptions?.().locale || (typeof navigator !== "undefined" ? navigator.language : "") || "en";
  return raw.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function createI18n(locale) {
  const current = dictionaries[locale] ?? dictionaries.en;
  function getByPath(path) {
    return path.split(".").reduce((acc, key) => (acc ? acc[key] : undefined), current);
  }
  function interpolate(value, params = {}) {
    if (typeof value !== "string") return value;
    return value.replace(/\{(\w+)\}/g, (_, key) => `${params[key] ?? ""}`);
  }
  return {
    locale,
    dict: current,
    t(path, params) {
      const value = getByPath(path);
      return interpolate(value ?? path, params);
    },
  };
}
